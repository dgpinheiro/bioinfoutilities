use strict;
use warnings;
use Readonly;
use Getopt::Long;
use File::Basename;


use vars qw/$LOGGER/;

INIT {
    use Log::Log4perl qw/:easy/;
    Log::Log4perl->easy_init($FATAL);
    $LOGGER = Log::Log4perl->get_logger($0);
}

my ($level, $indexdir);

Usage("Too few arguments") if $#ARGV < 0;
GetOptions( "h|?|help" => sub { &Usage(); },
            "l|level=s"=> \$level,
            "i|index=s"=>\$indexdir
    ) or &Usage();


if ($level) {
    my %LEVEL = (   
    'OFF'   =>$OFF,
    'FATAL' =>$FATAL,
    'ERROR' =>$ERROR,
    'WARN'  =>$WARN,
    'INFO'  =>$INFO,
    'DEBUG' =>$DEBUG,
    'TRACE' =>$TRACE,
    'ALL'   =>$ALL);
    $LOGGER->logdie("Wrong log level ($level). Choose one of: ".join(', ', keys %LEVEL)) unless (exists $LEVEL{$level});
    Log::Log4perl->easy_init($LEVEL{$level});
}

foreach my $f (glob("$indexdir/*.fa")) {
    my $bn=basename($f,'.fa');
    open(IN, "<", $f) or $LOGGER->logdie($!);
    while(<IN>) {
        chomp;
        if ($_=~/^>/) {
            if ($_=~/(GB\d+\-[RP][A-Z](?:_\d+)?)/) {
                my $pepid=$1;
                $pepid=~s/-R/-P/;
                my $rnaid=$pepid;
                $rnaid=~s/-P/-R/;
                print $bn,"\t",$pepid,"\t",$rnaid,"\n";
            }
            elsif ($_=~/([XN]P_\d+)/) {
                my $pepid = $1;
                my ($rnaid) = $_=~/([^\|]+)$/;
                $LOGGER->logdie("NOT FOUND RNA ($_)") unless($rnaid);
                print $bn,"\t",$pepid,"\t",$rnaid,"\n";
            }
            else {
                $LOGGER->logdie("Pattern not considered: $_");
            }
        }            
    }
    close(IN);
}

# Subroutines

